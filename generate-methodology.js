function h1(text) { return new Paragraph({ heading: HeadingLevel.HEADING_1, spacing: { before: 360, after: 200 }, children: [new TextRun({ text, bold: true, size: 36, font: "Arial", color: "1a1a2e" })] }); }
function h2(text) { return new Paragraph({ heading: HeadingLevel.HEADING_2, spacing: { before: 280, after: 160 }, children: [new TextRun({ text, bold: true, size: 28, font: "Arial", color: "16213e" })] }); }
function h3(text) { return new Paragraph({ heading: HeadingLevel.HEADING_3, spacing: { before: 200, after: 120 }, children: [new TextRun({ text, bold: true, size: 24, font: "Arial", color: "0f3460" })] }); }
function p(text) { return new Paragraph({ spacing: { after: 120 }, children: [new TextRun({ text, size: 22, font: "Arial" })] }); }
function pb(label, text) { return new Paragraph({ spacing: { after: 80 }, children: [new TextRun({ text: label, bold: true, size: 22, font: "Arial" }), new TextRun({ text, size: 22, font: "Arial" })] }); }
function code(text) { return new Paragraph({ spacing: { after: 80 }, children: [new TextRun({ text, size: 20, font: "Consolas", color: "333333" })] }); }
function bullet(text, ref) { return new Paragraph({ numbering: { reference: ref, level: 0 }, spacing: { after: 60 }, children: [new TextRun({ text, size: 22, font: "Arial" })] }); }
function spacer() { return new Paragraph({ spacing: { after: 80 }, children: [] }); }

function fnRow(name, line, desc) {
  return new TableRow({ children: [
    new TableCell({ borders, width: { size: 2800, type: WidthType.DXA }, margins: cellM, children: [new Paragraph({ children: [new TextRun({ text: name, bold: true, size: 20, font: "Consolas" })] })] }),
    new TableCell({ borders, width: { size: 800, type: WidthType.DXA }, margins: cellM, children: [new Paragraph({ children: [new TextRun({ text: String(line), size: 20, font: "Arial", color: "666666" })] })] }),
    new TableCell({ borders, width: { size: 5760, type: WidthType.DXA }, margins: cellM, children: [new Paragraph({ children: [new TextRun({ text: desc, size: 20, font: "Arial" })] })] }),
  ]});
}

function fnTable(rows) {
  return new Table({
    width: { size: 9360, type: WidthType.DXA },
    columnWidths: [2800, 800, 5760],
    rows: [
      new TableRow({ children: [
        new TableCell({ borders, width: { size: 2800, type: WidthType.DXA }, margins: cellM, shading: { fill: "1a1a2e", type: ShadingType.CLEAR }, children: [new Paragraph({ children: [new TextRun({ text: "Function", bold: true, size: 20, font: "Arial", color: "FFFFFF" })] })] }),
        new TableCell({ borders, width: { size: 800, type: WidthType.DXA }, margins: cellM, shading: { fill: "1a1a2e", type: ShadingType.CLEAR }, children: [new Paragraph({ children: [new TextRun({ text: "Line", bold: true, size: 20, font: "Arial", color: "FFFFFF" })] })] }),
        new TableCell({ borders, width: { size: 5760, type: WidthType.DXA }, margins: cellM, shading: { fill: "1a1a2e", type: ShadingType.CLEAR }, children: [new Paragraph({ children: [new TextRun({ text: "Description", bold: true, size: 20, font: "Arial", color: "FFFFFF" })] })] }),
      ]}),
      ...rows
    ]
  });
}

